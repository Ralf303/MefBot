const deleteItem = async (user, id, ctx) => {
  const item = await Item.findOne({
    where: {
      id: id,
      userId: user.id,
    },
  });

  if (!item) {
    await ctx.reply(`–£ –≤–∞—Å –Ω–µ—Ç —Ç–∞–∫–æ–π –≤–µ—â–∏üò•`);
    return;
  }
  const havePups = await checkItem(user.id, "–ü—É–ø—Å ¬´–ë–∞—Ä—Ç–µ—Ä¬ª");
  let cashBack;
  if (havePups) {
    cashBack = item.price;
  } else {
    cashBack = item.price / 2;
  }

  await ctx.reply(
    `–£—Å–ø–µ—à–Ω–æ —É–¥–∞–ª–µ–Ω–∞ –≤–µ—â—å ${item.itemName}[${item.id}]\n–í—ã –ø–æ–ª—É—á–∏–ª–∏ ${cashBack}`
  );
  await loseLog(user, `${item.itemName}[${item.id}]`, `–£–¥–∞–ª–µ–Ω–∏–µ`);
  user.balance += cashBack;
  user.fullSlots--;
  await item.destroy();
  await user.save();
};

