const getInventory = async (user, ctx) => {
  const items = await user.getItems();
  if (items.length === 0) {
    await ctx.reply("–¢–≤–æ–π –∏–Ω–≤–µ–Ω—Ç–∞—Ä—å –ø—É—Å—Ç.");
    return;
  }
  const itemNames = items.map(
    (item) => `${item.itemName}[<code>${item.id}</code>]`
  );
  const rows = [];
  for (let i = 0; i < itemNames.length; i += 2) {
    let row = itemNames[i];

    if (i + 1 < itemNames.length) {
      row += `, ${itemNames[i + 1]}`;
    }
    rows.push(row);
  }
  await ctx.replyWithHTML(
    `–¢–≤–æ–π –∏–Ω–≤–µ–Ω—Ç–∞—Ä—å:\n${rows.join(
      "\n"
    )}\n\nüìñ–ù–∞–¥–µ—Ç—å id\nüìñ–£–¥–∞–ª–∏—Ç—å –≤–µ—â—å id\nüìñ–ü–µ—Ä–µ–¥–∞—Ç—å –≤–µ—â—å id`
  );
};

